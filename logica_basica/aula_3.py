# Tipos de dados em Python

"""
Python -> Linguagem de Programação
Tipo de Tipagem -> Dinâmica / Forte

str -> String -> Dado de texto
São dados de texto que se encontram dentro de aspas
"""

# Aspas simples
print('Olá, Mundo!')

# Aspas duplas
print("Meu nome é Pedro.")

# Escape 
print('\"Programador Treinee\"') # Geralmente é qualquer dado seguido do '\'. (Ex.: \, /, ', ")

# r
print(r'\"Programador Treinee\"') # Formatador do print, além de ser usado para expressões regulares