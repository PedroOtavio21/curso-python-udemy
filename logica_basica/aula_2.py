# Função print()

"""
Usada para exibir comandos na tela do computador
Recebe argumentos para serem exibidos, sendo separados por ','
"""

print('Olá, Mundo!')

# Argumentos extras

"""
sep
Usado quando para separar os argumentos, separados por virgula na função print()
"""

print(12, 3, 2025, sep='/') # Printa na tela a data atual

"""
end
Argumento que coloca na tela um determinado argumento, ao final da linha
"""

print('Valor de salário recebido', end=':\n') # coloca os ':' e pula a linha
print('R$', 5580.00, sep=' ')